#!/usr/bin/env python3
"""
This module provides a function to add two floats.

The add function takes two float arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Add two floats and return their sum.

    Args:
        a (float): The first float to add.
        b (float): The second float to add.

    Returns:
        float: The sum of the two floats.
    """
    return a + b
