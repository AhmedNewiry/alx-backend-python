#!/usr/bin/env python3
"""
This module provides a function to convert a float to its string representation.

The to_str function takes a float argument and returns its string representation.
"""


def to_str(n: float) -> str:
    """
    Return the string representation of a float.

    Args:
        n (float): The float number to convert to string.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
